const translations = { en, id, ar };

type TranslationKey = keyof typeof en;

const useTranslation = () => {
  const { settings } = useSettings();
  const lang = settings.language;

  const t = (key: TranslationKey, options?: { [key: string]: string | number }): string => {
    let text = (translations[lang] as any)[key] || (translations['en'] as any)[key];
    if (options) {
      Object.keys(options).forEach(optionKey => {
        text = text.replace(new RegExp(`{{${optionKey}}}`, 'g'), String(options[optionKey]));
      });
    }
    return text;
  };

  return { t, currentLang: lang };
};

export default useTranslation;